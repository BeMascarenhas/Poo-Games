Obstacle::Obstacle(Vector2 position)
{
    this -> position = position;  

    

    for(  unsigned i = 0; i < grid.size(); i++)
    {
        for( unsigned j = 0; j < grid[0].size(); j++)
        {
            if(grid[i][j] == 1)
            {
                float pos_x = position.x + j * 3;//* multiplicado por 3 devido ao tamanho do pixel
                float pos_y = position.y + i * 3;

                Block block = Block({pos_x,pos_y});
                blocks.push_back(block);  
            }
        }
    }
}

void Obstacle::Draw()
{
    for(auto& block : blocks)
    {
        block.Draw();
    }
}